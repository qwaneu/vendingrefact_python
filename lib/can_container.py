class CanContainer:
  def setType(self, t):
    self.the_type = t

  def type(self):
    return self.the_type

  def setPrice(self, p):
    self.p = p

  def price(self):
    return self.p

  def setAmount(self,amount):
    self.amount = amount

  def amount(self):
    return self.amount




